


public class Book {
    int ID;
    String name;
    String genre;
    int year;
    int numberOfPages;

    public Book(int id, String name, String genre, int year, int numberOfPages){
        this.ID = id;
        this.name = name;
        this.genre = genre;
        this.year = year;
        this.numberOfPages = numberOfPages;
    }
}
